#include "libft.h"

char	*ft_strstr(const char *s1, const char *s2)
{
	int	i;
	int f;
	int j;

	i = 0;
	f = 0;
	j = 0;
	if ((*s2 == '\0') || ((*s1 == '\0' && *s2 == '\0')))
		return ((char*)s1);
	while (s1[i] != '\0')
	{
		j = 0;
		while (s1[i] == s2[j])
		{
			j++;
			i++;
			if (s2[j] == '\0')
				return ((char *)&s1[i - j]);
		}
		i = i - j;
		i++;
	}
	return (0);
}

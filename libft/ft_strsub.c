#include "libft.h"

char	*ft_strsub(char const *s, unsigned int start, size_t len)
{
	char			*str;
	unsigned int	i;

	if (s)
	{
		i = 0;
		str = (char*)malloc(sizeof(char) * len + 1);
		if (str == 0)
			return (NULL);
		while (s[start] != '\0' && i < len)
		{
			str[i] = s[start];
			start++;
			i++;
		}
		str[i] = '\0';
		return (str);
	}
	return (NULL);
}
